package quiz;

public class A06_Calendar {

	public static void main(String[] args) {
		
		
		System.out.printf("%-8s%-8s%-8s%-8s%-8s%-8s%-8s\n", 
				"Sun", "Mon", "Tue", "Wed", "Thu", "Fri", "Sat");
		
		System.out.println();
		
		System.out.printf("\t\t\t\t\t\t%02d\n", 1);
		
		System.out.printf("%02d\t%02d\t%02d\t%02d\t%02d\t%02d\t%02d\n", 2, 3, 4, 5, 6, 7, 8);
		System.out.printf("%02d\t%02d\t%02d\t%02d\t%02d\t%02d\t%02d\n", 9, 10, 11, 12, 13, 14, 15);
		System.out.printf("%02d\t%02d\t%02d\t%02d\t%02d\t%02d\t%02d\n", 16, 17, 18, 19, 20, 21, 22);
		System.out.printf("%02d\t%02d\t%02d\t%02d\t%02d\t%02d\t%02d\n", 23, 24, 25, 26, 27, 28, 29);
		System.out.printf("%02d\t%02d\n", 30, 31);
		
		
		System.out.println("----------------------------------------------------");
		
		
	}
}
