package quiz;

public class A03_VariableTypes {
	
	public static void main(String[] args) {
		
		String text = "홍길동";
		byte age = 20;
		String number = "010-1234-1234";
		double tall = 178.5;
		byte weight = 75;
		char blood = 'O';
		
		System.out.println("===========출력 결과===========\n");
		System.out.println("이름 : " + text);
		System.out.println("나이 : " + age);
		System.out.println("Tel : " + number);
		System.out.println("키 : " + tall);
		System.out.println("몸무게 : " + weight);
		System.out.println("혈액형 : " + blood);
		
		
	}
}
